#Spam = To see the position of element in List
a = [1,4,5,69]
print(a)
print("Location of 4 is",a.index(4))

#Append = To add element at end of List
a.append(6969)
print("APPEND",a)

#Insert =  To add element at any position of List
a.insert(0,111)
print("INSERT",a)

#Remove =  To remove a certain element from List
r = int(input("WHAT TO REMOVE:"))
a.remove(r) 
print(a)

#Sort = To filter list in Ascending, Descending or any other order
a.sort()
print(a)